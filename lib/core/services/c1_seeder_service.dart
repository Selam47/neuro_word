  Future<int> syncCleanC1Data() async {
    final existingWords = await _fetchExistingC1EnWords();

    final toAdd = _cleanC1Words.where((w) {
      final en = (w['en'] as String).toLowerCase().trim();
      return en.isNotEmpty && !existingWords.contains(en);
    }).toList();

    if (toAdd.isEmpty) return 0;

    int addedCount = 0;

    for (int i = 0; i < toAdd.length; i += _batchSize) {
      final end = (i + _batchSize < toAdd.length)
          ? i + _batchSize
          : toAdd.length;
      final chunk = toAdd.sublist(i, end);
      final WriteBatch batch = _firestore.batch();

      for (final wordData in chunk) {
        final docRef = _firestore.collection(_collection).doc();
        batch.set(docRef, {
          'en': wordData['en'],
          'tr': wordData['tr'],
          'pos': wordData['pos'],
          'level': 'C1',
          'cefr': 'C1',
          'category': wordData['category'],
          'difficultyWeight': wordData['difficultyWeight'],
        });
        addedCount++;
      }

      await batch.commit();
    }

    return addedCount;
  }

  Future<void> deleteAllC1Words() async {
    const pageSize = 499;
    bool hasMore = true;
    DocumentSnapshot? lastDoc;

    while (hasMore) {
      Query query = _firestore
          .collection(_collection)
          .where('level', isEqualTo: 'C1')
          .limit(pageSize);

      if (lastDoc != null) {
        query = query.startAfterDocument(lastDoc);
      }

      final snapshot = await query.get();

      if (snapshot.docs.isEmpty) {
        hasMore = false;
        break;
      }

      final WriteBatch batch = _firestore.batch();
      for (final doc in snapshot.docs) {
        batch.delete(doc.reference);
      }
      await batch.commit();

      if (snapshot.docs.length < pageSize) {
        hasMore = false;
      } else {
        lastDoc = snapshot.docs.last;
      }
    }
  }

  Future<Set<String>> _fetchExistingC1EnWords() async {
    final snapshot = await _firestore
