  Future<int> purgeLowQualityA1Words() async {
    const pageSize = 499;
    bool hasMore = true;
    DocumentSnapshot? lastDoc;
    int deletedCount = 0;

    while (hasMore) {
      Query query = _firestore
          .collection(_collection)
          .where('level', isEqualTo: 'A1')
          .limit(pageSize);

      if (lastDoc != null) {
        query = query.startAfterDocument(lastDoc);
      }

      final snapshot = await query.get();

      if (snapshot.docs.isEmpty) {
        hasMore = false;
        break;
      }

      final toDelete = snapshot.docs.where((doc) {
        final en = (doc.data() as Map<String, dynamic>)['en'] as String? ?? '';
        return _lowQualityA1Words.contains(en.toLowerCase().trim());
      }).toList();

      if (toDelete.isNotEmpty) {
        final WriteBatch batch = _firestore.batch();
        for (final doc in toDelete) {
          batch.delete(doc.reference);
          deletedCount++;
        }
        await batch.commit();
      }

      if (snapshot.docs.length < pageSize) {
        hasMore = false;
      } else {
        lastDoc = snapshot.docs.last;
      }
    }

    return deletedCount;
  }
